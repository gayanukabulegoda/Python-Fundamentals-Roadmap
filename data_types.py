print ("------- Data Types | type() -------\n")

a = 10
print (a)
print (type(a))
print (a, type(a))

print ("\n------- Print() -------\n")

print ("The type of variable having value", a, "is: ", type(a))

print ("\n------- Scientific notation -------\n")

a = 2.5e4
print (a, type(a))

print ("\n------- Type casting -------\n")

a = 5
b = float(a)
print (b, type(b))

c = 7.0
d = int(c)
print (d, type(d))
